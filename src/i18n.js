const LANG_NAMES = { ko: '🇰🇷 한국어', en: '🇺🇸 English', zh: '🇨🇳 中文', es: '🇪🇸 Español', ja: '🇯🇵 日本語' };

function detectLang() {
  const nav = navigator.language || navigator.userLanguage || 'en';
  const short = nav.split('-')[0].toLowerCase();
  return SUPPORTED_LANGS.includes(short) ? short : 'en';
}

const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(() => {
    try { const saved = localStorage.getItem('dtb-lang'); if (saved && SUPPORTED_LANGS.includes(saved)) return saved; } catch (e) {}
    return detectLang();
  });

  const setLang = useCallback((l) => {
    if (!SUPPORTED_LANGS.includes(l)) return;
    setLangState(l);
    try { localStorage.setItem('dtb-lang', l); } catch (e) {}
  }, []);

  const t = translations[lang] || translations.en;

  return createElement(I18nContext.Provider, { value: { lang, setLang, t, SUPPORTED_LANGS, LANG_NAMES } }, children);
}

export { I18nContext, SUPPORTED_LANGS, LANG_NAMES };

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
}
